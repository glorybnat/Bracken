    <script>
        function showChatBox(chatBoxId, button) {
            document.querySelectorAll('.about-button').forEach(btn => {
                btn.classList.add('moved');
            });
            document.querySelectorAll('.chat-box').forEach(chatBox => {
                chatBox.style.display = 'none';
            });
            const chatBox = document.getElementById(chatBoxId);
            chatBox.style.display = 'block';
            const buttonRect = button.getBoundingClientRect();
            const chatBoxRect = chatBox.getBoundingClientRect();
            const tail = chatBox.querySelector('.tail');
            tail.style.left = (buttonRect.left + buttonRect.width / 2.5 - chatBoxRect.left) + 'px';
            console.log(button.innerText + " button was clicked.");
        }

        document.querySelector('.burger-menu').addEventListener('click', function () {
            document.querySelector('.menu-links').classList.toggle('active');
